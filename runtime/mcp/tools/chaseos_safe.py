def chaseos_query_sic_evidence(
    params: dict[str, Any],
    config: MCPConfig,
    envelope: PermissionEnvelope,
) -> HandlerResult:
    query = params.get("query")
    if not isinstance(query, str) or not query.strip():
        return HandlerResult(False, error=input_error("invalid_query", "query must be a non-empty string"))
    readme_rel = "runtime/source_intelligence/README.md"
    readme = config.vault_root / readme_rel
    excerpt = readme.read_text(encoding="utf-8", errors="replace")[:1200] if readme.exists() else ""
    return HandlerResult(
        True,
        {
            "tool": "chaseos.query_sic_evidence",
            "query": query.strip(),
            "mode": "summary_stub",
            "evidence": [{"source": readme_rel, "excerpt": excerpt}] if excerpt else [],
            "canonical_writeback": False,
        },
        files_read=[readme_rel] if excerpt else [],
        audit_metadata={"tool": "chaseos.query_sic_evidence", "query_length": len(query.strip())},
    )

